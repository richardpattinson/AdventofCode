        diff = sorted_data[x+1] - sorted_data[x]
        if diff == 1 
            step_one += 1
        elsif diff == 2
            step_two += 1
        elsif diff == 3 
            step_three += 1
        end
    end
    p step_one 
    p step_two
    p step_three
    return step_one * step_three


end

